int	ft_str_is_alpha(char *st)
{
	int i;

	i = 0;
	if (st[i] == '\0')
		return (1);
	else
	{
		while (st[i] != '\0')
		{
			if ((st[i] >= 65 && st[i] <= 90) || (st[i] >= 97 && st[i] <= 122))
			{
				i++;
			}
			else
				return (0);
		}
		return (1);
	}
}
