void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int aux;
	int higher;

	i = 0;
	higher = size - 1;
	while (i < higher)
	{
		aux = tab[i];
		tab[i] = tab[higher];
		tab[higher] = aux;
		i++;
		higher--;
	}
}
