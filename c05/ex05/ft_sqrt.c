int	ft_sqrt(int nb)
{
	int res;

	res = 1;
	if (nb == 1)
		return (nb);
	while (res < nb && res <= 46340)
	{
		if (res * res == nb)
			return (res);
		else
			res++;
	}
	return (0);
}
